public class Operators{
    @SuppressWarnings("unused")
    public static void main(String args[])
    {
        int x = 100, y = 20;

        System.out.println("x = " + x + "\ny = " + y);
        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (x / y));
        System.out.println("x % y = " + (x % y));
        System.out.println("x > y = " + (x > y));

    }
}